/* 2교시: 기본 타입과 참조 타입의 이해 */

public class GreetingApp {
    /*
     자료형
     : 기본 타입은 값을 담는 그릇, 참조 타입은 주소값을 담는 그릇
     - 기본타입 (디테일하게 9개만 외우기)
       - 숫자
         - 정수: byte, short, int, long
         - 실수: float, double
       - 문자: char
       - 문자열: String
       - 논리: boolean (true | false)

     - 참조타입: 기본타입이 아닌 모든 타입을 의미한다

     */
    public static void main(String [] args) { // 컴파일도 되고 인터프레팅도 됨. 엔트리 포인트
        System.out.println("섭섭이와 함께하는 즐거운 자바!!");
    }
}